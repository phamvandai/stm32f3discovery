FATFS 		SD_FatFs;
FIL	 		f;
FRESULT		fres;
char 		SD_Path[4]; /* SD card logical drive path */
char 		test_buf[512];
uint32_t 	cycle_count;
uint32_t	tick, tick_count;
int			err_cnt = 0;

static int SD_Config(void);
/**
  * @brief  The application entry point.
  *
  * @retval None
  */
int main(void)
{
  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_USART1_UART_Init();

  debug_print("\r\n\r\nTest fatfs SPI\r\n");

  if(SD_Config() == 0) {
	  int i = 0, j = 0;
	  UINT		bw, br;

	  if(f_open(&f, "test.txt", FA_OPEN_APPEND | FA_WRITE) == FR_OK) {
		  debug_print("Open test.txt done\r\n");

		  f_printf(&f, "STM32F3 DISCOVERY SD CARD with SPI2\r\n");
		  for(i = 0; i < 10; i++) {
			  f_printf(&f, "<%d>\r\n", i);
		  }

		  f_close(&f);

		  debug_print("Write to SD done\r\n");

	  }
	  else {
		  debug_print("Open test.txt failed\r\n");
	  }

	  /* Fill test data */
	  for(i = 0; i < 512; i++) {
		  test_buf[i] = (char) i;
	  }

	  /* 2 MB Write test*/
	  debug_print("1MB Write test\r\n");
	  if((fres = f_open(&f, "test.bin", FA_WRITE | FA_CREATE_ALWAYS)) == FR_OK) {
		  tick = HAL_GetTick();

		  for(j = 0; j < 2048; j++) {
			  if(f_write(&f, test_buf, 512, &bw) != FR_OK) {
				  debug_printf("Write block %d failed\r\n", j);
				  break;
			  }
			  if(bw != 512) {
				  debug_printf("Actual block write size %d\r\n", bw);
			  }
		  }

		  tick_count = HAL_GetTick() - tick;
		  f_close(&f);
		  debug_printf("done, %d ms\r\n", tick_count);
	  }
	  else {
		  debug_printf("Open test.bin failed %d\r\n", fres);
	  }

	  /* Read test*/
	  debug_print("1MB Read test\r\n");
	  if(f_open(&f, "test.bin", FA_READ) == FR_OK) {
		  tick = HAL_GetTick();

		  for(j = 0; j < 2048; j++) {
			  if(f_read(&f, test_buf, 512, &br) != FR_OK) {
				  debug_printf("Read block %d failed\r\n", j);
				  break;
			  }
			  if(br != 512) {
				  debug_printf("Actual block read size %d\r\n", br);
			  }
			  for(i = 0; i < 512; i++) {
				  if(test_buf[i] != (char)i) {
					  err_cnt++;
				  }
			  }
		  }

		  tick_count = HAL_GetTick() - tick;
		  f_close(&f);
		  debug_printf("done, %d ms\r\n", tick_count);
		  if(err_cnt) {
			  debug_printf("%d errors\r\n", err_cnt);
		  }
	  }
	  else {
		  debug_print("Open test.bin failed\r\n");
	  }
  }

  while (1)
  {

  }

}

/**
  * @brief System Clock Configuration
  * @retval None
  */
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;
  RCC_PeriphCLKInitTypeDef PeriphClkInit;

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSI|RCC_OSCILLATORTYPE_HSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_BYPASS;
  RCC_OscInitStruct.HSEPredivValue = RCC_HSE_PREDIV_DIV1;
  RCC_OscInitStruct.HSIState = RCC_HSI_ON;
  RCC_OscInitStruct.HSICalibrationValue = 16;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLMUL = RCC_PLL_MUL6;
  if (HAL_RCC_OscConfig(&RCC_OscInitStruct) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Initializes the CPU, AHB and APB busses clocks 
    */
  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV2;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV1;

  if (HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_1) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

  PeriphClkInit.PeriphClockSelection = RCC_PERIPHCLK_USART1|RCC_PERIPHCLK_I2C1;
  PeriphClkInit.Usart1ClockSelection = RCC_USART1CLKSOURCE_PCLK2;
  PeriphClkInit.I2c1ClockSelection = RCC_I2C1CLKSOURCE_HSI;
  if (HAL_RCCEx_PeriphCLKConfig(&PeriphClkInit) != HAL_OK)
  {
    _Error_Handler(__FILE__, __LINE__);
  }

    /**Configure the Systick interrupt time 
    */
  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

    /**Configure the Systick 
    */
  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

  /* SysTick_IRQn interrupt configuration */
  HAL_NVIC_SetPriority(SysTick_IRQn, 0, 0);
}

/* I2C1 init function */
